#pragma once

namespace Renderer
{
    /**
     * 纹理单元分配方案
     *
     * 设计目标：
     * - 为ImGui预留纹理单元0（避免冲突）
     * - 为材质纹理预留连续单元1-3
     * - 为环境光预留单元10
     * - 为天空盒预留单元15
     *
     * 分配原则：
     * - 纹理单元 0: ImGui字体（ImGui默认使用）
     * - 纹理单元 1-9: 3D场景纹理
     * - 纹理单元 10-15: 环境纹理
     * - 纹理单元 16+: 阴影/其他高级功能
     */
    enum class TextureUnit : int
    {
        // ========================================
        // ImGui UI层（预留）
        // ========================================
        IMGUI_FONT = 0,           // ImGui字体纹理

        // ========================================
        // 3D场景材质纹理
        // ========================================
        MATERIAL_DIFFUSE = 1,     // 漫反射纹理（从单元0移动）
        MATERIAL_NORMAL = 2,      // 法线纹理
        MATERIAL_SPECULAR = 3,    // 高光纹理
        MATERIAL_HEIGHT = 4,      // 高度纹理
        MATERIAL_EMISSION = 5,    // 自发光纹理

        // ========================================
        // 环境纹理
        // ========================================
        AMBIENT_SKYBOX = 10,      // 环境光天空盒

        // ========================================
        // 天空盒渲染
        // ========================================
        SKYBOX_CUBEMAP = 15,      // 天空盒立方体贴图

        // ========================================
        // 高级功能（预留）
        // ========================================
        SHADOW_MAP_0 = 16,        // 阴影贴图0
        SHADOW_MAP_1 = 17,        // 阴影贴图1
        SHADOW_MAP_2 = 18,        // 阴影贴图2
        // ... 更多阴影贴图
    };

} // namespace Renderer
